// Start a for loop with an initializer, condition, and incrementer
for (let i = 0; i < 5; i++) { 
    // 'let i = 0' initializes the loop counter variable 'i' to 0
    // 'i < 5' is the condition that keeps the loop running as long as 'i' is less than 5
    // 'i++' increments the value of 'i' by 1 after each iteration

    // Log the current iteration number to the console
    console.log("Iteration number: " + i); 
    // The string "Iteration number: " is concatenated with the current value of 'i'
    // This outputs the iteration number for each loop cycle
}
