// Declare a variable 'a' and assign it the value 10
let a = 10;

// Declare a variable 'b' and assign it the value 5
let b = 5;

// Perform addition of 'a' and 'b' and log the result (10 + 5 = 15)
console.log(a + b);  // Addition: 15

// Perform subtraction of 'b' from 'a' and log the result (10 - 5 = 5)
console.log(a - b);  // Subtraction: 5

// Perform multiplication of 'a' and 'b' and log the result (10 * 5 = 50)
console.log(a * b);  // Multiplication: 50

// Perform division of 'a' by 'b' and log the result (10 / 5 = 2)
console.log(a / b);  // Division: 2

// Perform modulus operation (remainder of 'a' divided by 'b') and log the result (10 % 5 = 0)
console.log(a % b);  // Modulus (remainder): 0

// Perform exponentiation (a raised to the power of b) and log the result (10^5 = 100000)
console.log(a ** b); // Exponentiation: 100000

// Declare a variable 'c' and assign it the value 5
let c = 5;

// Log the value of 'c' before incrementing it (post-increment: outputs 5, then increments 'c' to 6)
console.log(c++);    // Post-increment: outputs 5, then c becomes 6

// Increment 'c' first (pre-increment: increments 'c' to 7) and then log the value
console.log(++c);    // Pre-increment: c becomes 7, then outputs 7

// Log the value of 'c' before decrementing it (post-decrement: outputs 7, then decrements 'c' to 6)
console.log(c--);    // Post-decrement: outputs 7, then c becomes 6

// Decrement 'c' first (pre-decrement: decrements 'c' to 5) and then log the value
console.log(--c);    // Pre-decrement: c becomes 5, then outputs 5

// Declare a variable 'str1' and assign it the string "Hello"
let str1 = "Hello";

// Declare a variable 'str2' and assign it the string "World"
let str2 = "World";

// Concatenate 'str1', a space, and 'str2', then log the resulting string ("Hello World")
console.log(str1 + " " + str2);  // Outputs: "Hello World"
