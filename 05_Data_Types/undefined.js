// Declare a variable named 'undefinedVariable' without assigning any value to it.
// By default, variables declared without an initial value are assigned the value 'undefined'.
let undefinedVariable;

// Log the value of 'undefinedVariable' to the console.
// Since no value was assigned, it will output 'undefined'.
console.log(undefinedVariable); // Outputs: undefined

// Log the type of 'undefinedVariable' to the console using the 'typeof' operator.
// The 'typeof' operator returns the type of the variable as a string.
// In this case, it will output 'undefined' because the variable has not been assigned a value.
console.log(typeof undefinedVariable); // Outputs: undefined