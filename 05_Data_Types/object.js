// Define an object named 'person' with three properties: name, age, and isStudent
let person = {
    name: "John",       // 'name' property with the value "John" (string)
    age: 30,            // 'age' property with the value 30 (number)
    isStudent: false    // 'isStudent' property with the value false (boolean)
};

// Access and log the 'name' property of the 'person' object to the console
console.log(person.name); // Outputs: John

// Log the type of the 'person' object to the console
console.log(typeof person); // Outputs: object (in JavaScript, objects are of type 'object')

// Define an array named 'arr' with three numeric elements
let arr = [1, 2, 3]; // Arrays in JavaScript are a special type of object

// Log the type of the 'arr' array to the console
console.log(typeof arr); // Outputs: object (arrays are technically objects in JavaScript)
