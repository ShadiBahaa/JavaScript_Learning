// Declare a variable `bigNumber` and assign it a BigInt value.
// BigInt is a special numeric type in JavaScript that can represent integers of arbitrary precision.
// The `n` at the end of the number indicates that it is a BigInt.
let bigNumber = 1234567890123456789012345678901234567890n;

// Log the value of `bigNumber` to the console.
// This will output the BigInt value as it is, including the `n` suffix.
console.log(bigNumber); // Outputs: 1234567890123456789012345678901234567890n

// Log the type of `bigNumber` to the console using the `typeof` operator.
// This will output the string "bigint", indicating the type of the variable.
console.log(typeof bigNumber); // Outputs: bigint
