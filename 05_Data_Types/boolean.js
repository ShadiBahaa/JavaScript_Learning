// Declare a variable `isGreater` and assign it the result of the comparison `4 > 1`.
// This evaluates to `true` because 4 is indeed greater than 1.
let isGreater = 4 > 1;

// Log the value of `isGreater` to the console.
// This will output `true` since the comparison `4 > 1` is true.
console.log(isGreater); // Outputs: true

// Declare a variable `isLess` and assign it the result of the comparison `4 < 1`.
// This evaluates to `false` because 4 is not less than 1.
let isLess = 4 < 1;

// Log the value of `isLess` to the console.
// This will output `false` since the comparison `4 < 1` is false.
console.log(isLess);    // Outputs: false

// Use the `typeof` operator to determine the data type of the variable `isGreater`.
// Since `isGreater` holds a boolean value (`true`), this will output `boolean`.
console.log(typeof isGreater); // Outputs: boolean
